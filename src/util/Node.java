package util;


public class Node {
		   
		   private String pessoa;
		   private Node next;
		   
		public String getPessoa() {
			return pessoa;
		}
		
		public void setPessoa(String pessoa) {
			this.pessoa = pessoa;
		}
		
		public Node getNext() {
			return next;
		}
		
		public void setNext(Node next) {
			this.next = next;
		} 

}
